package cn.sichu.graduate_practice.easy;

public class ListNode {
    int val;
    ListNode next;

    public ListNode() {
        super();
    }

    public ListNode(int val) {
        super();
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        super();
        this.val = val;
        this.next = next;
    }

}
